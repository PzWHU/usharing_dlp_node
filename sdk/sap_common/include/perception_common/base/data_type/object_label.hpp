inline apStatus_t CheckLabelStringValid(const std::string& class_label_string) {
    if (kLabelToNum.find(class_label_string) == kLabelToNum.end()) {
        AP_LERROR(CheckLabelStringValid) << "class_label string not found in "
                                            "kLabelToNum, invalid "
                                            "class_label_string";
        AP_CHECK_ERROR(AP_INVALID_PARAM);
    }
    return AP_SUCCESS;
}

